"""Solution variants for Lesson 16: Lists Basics"""

def create_shopping_list(item1, item2, item3):
    return [item1, item2, item3]

def get_first_item(shopping_list):
    return shopping_list[0]

def get_last_item(shopping_list):
    return shopping_list[-1]

def how_many(shopping_list):
    return len(shopping_list)
